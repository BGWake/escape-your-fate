	public void stats(Events e) {
		if (e.getDogCount() > 0 || e.isFirstNumber() || e.isSecondNumber() || e.isThirdNumber() || e.isGoodGuess()) {
			System.out.println(stats());
		}
		if (e.getDogCount() > 1) {
			System.out.println(dogCount(e));
		}
		if (e.getDogCount() == 1) {
			System.out.println(dogCountOne(e));
		}
		if (e.isFirstNumber()) {
			System.out.println(firstNumber());
		}
		if (e.isSecondNumber()) {
			System.out.println(secondNumber());
		}
		if (e.isThirdNumber()) {
			System.out.println(thirdNumber());
		}
		if (e.isGoodGuess()) {
			System.out.println(goodGuess());
		}
	}
